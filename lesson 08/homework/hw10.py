numbers = [10, 20, 30, 40, 50, 60]
print("First three elements:", numbers[0:3])  # 0,1,2
print("Elements from index 2 to end:", numbers[2:])  # 2,3,4,5
print("Last two elements:", numbers[-2:])  # 4,5
print("Every second element:", numbers[::2])  # 0,2,4
print("Reversed list:", numbers[::-1])  # 5,4,3,2,1,0