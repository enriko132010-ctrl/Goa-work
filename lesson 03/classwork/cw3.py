#3) ორივე კოდი ახსენით კომენტარებით


## მომხმარებელს სთხოვს შეიყვანოს ორი პროდუქტი
product1 = input("შეიყვანე პირველი პროდუქტი: ")  # პირველი პროდუქტი ინახება product1-ში
product2 = input("შეიყვანე მეორე პროდუქტი: ")    # მეორე პროდუქტი ინახება product2-ში

# ბეჭდავს ორივე პროდუქტს ერთად
print("შენი პროდუქტებია:", product1, "და", product2)


#1) input() — გამოიყენება მომხმარებლის მიერ ინფორმაციის შესაყვანად.

#2) თითოეულ პროდუქტს ცალკე ვანახავთ ცვლადში (product1, product2).

#2) print() — აჩვენებს ეკრანზე შეყვანილ პროდუქტებს.



#კოდი 2 — პროდუქტი შეაქვს მანამ, სანამ არ ჩაწერს 'exit', ყველა პროდუქტი ემატება სიაში:

products = []  # ვქმნით ცარიელ სიას, სადაც ყველა პროდუქტი დაიკავებს ადგილს

product = ""   # ცვლადი, სადაც მომხმარებლის ბოლო შეყვანილი პროდუქტი ინახება

while product != "exit":  # ციკლი გაგრძელდება, სანამ მომხმარებელი არ ჩაწერს 'exit'
    product = input("შეიყვანე პროდუქტი (ჩაწერე 'exit' გასასვლელად): ")
    
    if product != "exit":   # თუ მომხმარებელმა არ ჩაწერა 'exit'
        products.append(product)  # ვამატებთ პროდუქტს სიაში

# დაბეჭდვა ბოლოს
print("შენი პროდუქტებია:", products)


#products = [] — ცარიელი სია პროდუქტებისთვის.

#while product != "exit" — ციკლი გრძელდება, სანამ მომხმარებელი არ ჩაწერს "exit".

#append() — გამოიყენება ელემენტის სიაში დამატებისთვის.

#საბოლოო print() — აჩვენებს ყველა შეყვანილ პროდუქტს ერთ სიაში.