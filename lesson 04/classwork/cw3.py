#3)  input საშუალებით შემოიტანეთ 2 რიცხვი ეს რიცხვები გარდაქმენით float შემდეგ გამოიყენეთ ამ რიცხვებზე ყველა 
# მათემატიკური ოპერაცია და შედარებები (კომენბატრებით ახსენით თითოეული 
# კოდის დეტალი რას აკეთებთ და რატომ გამოიყენეთ შესაბამისი ფუნქციები)


# მომხმარებელს სთხოვს შეიყვანოს პირველი რიცხვი
num1 = input("Enter the first number: ")  # input() ყოველთვის ტექსტს აბრუნებს
num1 = float(num1)  # ტექსტი გარდაქმნება float-ად, რათა შეგვიძლია მათემატიკური ოპერაციები

# მომხმარებელს სთხოვს შეიყვანოს მეორე რიცხვი
num2 = input("Enter the second number: ")
num2 = float(num2)  # ტექსტი გარდაქმნება float-ად

# ===== მათემატიკური ოპერაციები =====
print("Sum:", num1 + num2)              # +_ჯამი
print("Difference:", num1 - num2)       # -_სხვაობა
print("Product:", num1 * num2)          # *_ნამრავლი
print("Division:", num1 / num2)         # /_გაყოფა (float შედეგი)
print("Integer division:", num1 // num2) # //_მთლიანი გაყოფა (სულამდე)
print("Remainder:", num1 % num2)        # %_ნაშთი გაყოფისას
print("Exponent:", num1 ** num2)        # **_num1 აწევს num2 ხარისხზე

# ===== შედარებები =====
print("num1 == num2 ?", num1 == num2)   # ==_შემოწმებს ტოლია თუ არა
print("num1 != num2 ?", num1 != num2)   # !=_შემოწმებს განსხვავდება თუ არა
print("num1 > num2 ?", num1 > num2)     # >_შემოწმებს მეტია თუ არა
print("num1 < num2 ?", num1 < num2)     # <_შემოწმებს ნაკლებია თუ არა
print("num1 >= num2 ?", num1 >= num2)   # >=_შემოწმებს მეტია ან ტოლია
print("num1 <= num2 ?", num1 <= num2)   # <=_შემოწმებს ნაკლებია ან ტოლია
